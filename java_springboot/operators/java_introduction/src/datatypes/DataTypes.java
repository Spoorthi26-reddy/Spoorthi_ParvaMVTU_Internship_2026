package datatypes;

public class DataTypes {
	public static void main(String[] args) {
		// byte - range:-128 to 127
		byte age = 30;
		System.out.println("age is:" + age);

		// short - range:
		short age1 = 35;
		System.out.println("age is:" + age1);

		// int
		int n = 26;
		System.out.println("My birthday is " + n + "th of december");

		// long

		long a = 1000;
		System.out.println("the num is:" + a);

		// float
		float percent = 99.9f;
		System.out.println("the percentage is:" + percent);

		// double

		double marks = 77.80;
		System.out.println("marks is:" + marks);

		// char
		char gender = 'F';
		System.out.println("gender is:" +gender);
		
		//boolean
		
		boolean isActive = true;
		System.out.println("value is:" +isActive);
		
		

	}
}
